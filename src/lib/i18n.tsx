export const I18nProvider = ({ children }: { children: React.ReactNode }) => {
  const [lang, setLangState] = useState<Language>(() => {
    const saved = localStorage.getItem(LANG_KEY);
    return (saved as Language) || "zh";
  });

  const setLang = useCallback((newLang: Language) => {
    setLangState(newLang);
    localStorage.setItem(LANG_KEY, newLang);
  }, []);

  const t = useCallback((key: string): string => {
    return dictionaries[lang]?.[key] ?? dictionaries.zh[key] ?? key;
  }, [lang]);

  const locale = locales[lang];
  const dateFormat = dateFormats[lang];
  const shortDateFormat = shortDateFormats[lang];

  return (
    <I18nContext.Provider value={{ lang, setLang, t, locale, dateFormat, shortDateFormat }}>
      {children}
    </I18nContext.Provider>
  );
};

export const useI18n = (): I18nContextType => {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error("useI18n must be used within I18nProvider");
  return ctx;
};

/** Helper to interpolate {key} placeholders */
export const interpolate = (template: string, vars: Record<string, string | number>): string => {
  let result = template;
  for (const [key, value] of Object.entries(vars)) {
    result = result.replace(new RegExp(`\\{${key}\\}`, "g"), String(value));
  }
  return result;
};

/** Translate a category name (DB stores Chinese, display is localized) */
export const useCategoryName = () => {
  const { t } = useI18n();
  return useCallback((dbCategory: string): string => {
    return t(`category.${dbCategory}`) || dbCategory;
  }, [t]);
};
